**Introducción**
El proyecto tiene como objetivo ayudar a facilitar la detección temprana del cáncer de cuello uterino mediante
una prueba de automuestreo y con la ayuda de un asistente virtual. El cáncer de cuello uterino, causado por el  virus del papiloma humano (VPH), es un problema serio que afecta la salud y la vida de muchas mujeres en todo
el mundo, incluyendo nuestras comunidades.
Hay varios tipos de VPH. Algunos de estos tipos causan cáncer de cuello uterino, mientras que otros causan
verrugas genitales (pequeños bultos en los genitales, inofensivos pero muy molestos, esto no es cáncer). 
Además del contacto sexual, el virus también puede transmitirse mediante el contacto piel a piel.
A pesar de que en algunos países se ha avanzado en su control a través de exámenes de Papanicolaou y pruebas
de VPH, en nuestro país seguimos teniendo dificultades para reducir esta enfermedad. 
A pesar de que hay programas gratuitos, la tasa de cáncer de cuello uterino no ha disminuido en los últimos 20 años debido a la falta de educación sobre cómo prevenirlo y a obstáculos en la realización de pruebas.
Ahora hay un nuevo método para prevenir el cáncer de cuello uterino: en lugar de buscar células que hayan
cambiado, ahora podemos detectar el virus VPH en la muestra. Esto nos permite prevenir las etapas previas al
cáncer o detectarlas aún más temprano.
